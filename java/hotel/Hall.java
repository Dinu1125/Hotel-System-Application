package hotel;

public class Hall {

		int hallID;
		String hallName;
		
		public Hall(int hallID, String hallName) {
			
			this.hallID = hallID;
			this.hallName = hallName;
		}

		public int getHallID() {
			return hallID;
		}

		public void setHallID(int hallID) {
			this.hallID = hallID;
		}

		public String getHallName() {
			return hallName;
		}

		public void setHallName(String hallName) {
			this.hallName = hallName;
		}
		
		
}
